md"""
## Convolution

离散意义下的卷积的定义为：

```math
(X \star Y)[n_1, n_2, \dots, n_m] = \sum_t X[t_1, t_2, ..., t_m]Y[n_1-t_1, n_2-t_2, \dots, n_m-t_m]
```

实现一个二维情况下的卷积算子。

注： 当 X, Y 的尺寸不一致时， 可以通过填 ``0`` 的方式将尺寸变成一致. 一般将尺寸较小的称之为卷积核。

![convolution](https://miro.medium.com/max/1540/1*32zCSTBi3giSApz1oQV-zA.gif)

"""

# ╔═╡ 01bdfd29-04b8-4879-8491-76126c852e00
begin
	X1 = rand(1:5, 5, 5)
	Y1 = [0 1 0
		  1 -4 1
		  0 1 0] # 2D laplacian operator
end

# ╔═╡ a20312e4-21f3-41ac-a718-82d72d5edd32
function zero_pad_v1(X, sz)
	out = zeros(eltype(X), sz)
	c = sz .÷ 2 .+ 1
	r = size(X) .÷ 2
	out[c[1]-r[1]:c[1]+r[1], c[2]-r[2]:c[2]+r[2]] .= X
	out
end

# ╔═╡ 8c664146-048c-42c2-b5b3-5fc0ce32bc34
zero_pad_v1(X1, (7, 7))

# ╔═╡ 5ebebbd3-0e49-4366-878a-e9fc9df60cc4
function conv2D_v1(X, K)
	@assert all(isodd, size(K))

	out = similar(X)

	m, n = size(K)
	r = size(K) .÷ 2
	X_padded = zero_pad_v1(X, size(X) .+ size(K) .- 1)

	for i in axes(out, 1)
		for j in axes(out, 2)
			patch_X = X_padded[i:i+m-1, j:j+n-1]

			out[i, j] = sum(patch_X .* reverse(K))
		end
	end
	return out
end

# ╔═╡ a3858f8f-86aa-4276-aa7a-836fc0b0b28c
@benchmark conv2D_v1($X1, $Y1)

# ╔═╡ ea5a1056-bc0c-4ade-8dde-e5e40da5c941
function conv2D_v2(X, K)
	@assert all(isodd, size(K))

	out = similar(X)

	m, n = size(K)
	r = size(K) .÷ 2
	X_padded = zero_pad_v1(X, size(X) .+ size(K) .- 1)
	K = reverse(K)

	tmp = similar(K)

	for i in axes(out, 1)
		for j in axes(out, 2)
			patch_X = @view X_padded[i:i+m-1, j:j+n-1]

			tmp .= patch_X .* K
			out[i, j] = sum(tmp)
		end
	end
	return out
end

# ╔═╡ 0b59c19c-a414-4290-94c2-9af1701ed7b9
@benchmark conv2D_v2($X1, $Y1)

# ╔═╡ 45f6a3c8-edb4-4836-930e-0f0cadf870ba
function conv2D_v3(X, K)
	@assert all(isodd, size(K))

	out = similar(X)

	m, n = size(K)
	r = size(K) .÷ 2
	X_padded = zero_pad_v1(X, size(X) .+ size(K) .- 1)
	K = reverse(K)

	function sum2(X, Y)
		rst = zero(eltype(X))
		@inbounds @simd for i in eachindex(X, Y)
			rst += X[i] * Y[i]
		end
		return rst
	end

	for i in axes(out, 1)
		for j in axes(out, 2)
			patch_X = @view X_padded[i:i+m-1, j:j+n-1]

			out[i, j] = sum2(patch_X, K)
		end
	end
	return out
end

# ╔═╡ 123501dd-0c44-4658-bb1b-fc08917ccf1a
@benchmark conv2D_v3($X1, $Y1)

# ╔═╡ 1ab2e0f7-7672-4156-a775-107a360dcb25
function conv2D_v4(X, K)
	@assert all(isodd, size(K))

	out = similar(X)

	m, n = size(K)
	r = size(K) .÷ 2
	X_padded = zero_pad_v1(X, size(X) .+ size(K) .- 1)
	K = reverse(K)

	function sum2(X, Y)
		rst = zero(eltype(X))
		@inbounds @simd for i in eachindex(X, Y)
			rst += X[i] * Y[i]
		end
		return rst
	end

	for j in axes(out, 2)
		for i in axes(out, 1)
			patch_X = @view X_padded[i:i+m-1, j:j+n-1]

			out[i, j] = sum2(patch_X, K)
		end
	end
	return out
end

