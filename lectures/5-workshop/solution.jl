md"""
## Filter Bank

卷积是一种典型的滤波器 (filter)， 滤波器组的意思是将一堆相似的滤波器放在一起， 对单个输入进行处理后， 再分别输出结果。 假设 ``K = \{K_1, K_2, ..., K_p\}`` 是一组尺寸大小一致的卷积核， 并且定义它们的滤波器组的行为如下：

```math
(X \star K) = \{X \star K_1, X\star K_2, ..., X\star K_p\}
```

基于前面的卷积来实现一个 filter bank
"""

# ╔═╡ 842a7521-cf73-46b1-ab6e-6a42b447aebb
begin
	X3 = rand(1:5, 5, 5)
	K = [[0 1 0
		  1 -4 1
		  0 1 0],
		 [1/9 1/9 1/9
		  1/9 1/9 1/9
		  1/9 1/9 1/9
		 ]]
end

# ╔═╡ 77c5b269-9dbe-482d-a0b3-ae74b1db3370
map(K) do kern
	conv2D_v4(float.(X3), kern)
end

